    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String codigo, denominacion, primeraParada, ultimaParada;
        String frecuencia, paradasIntermedias;
        Linea miLinea;
        Paradas p1;
        Paradas p2;

        codigo = txtCodigo.getText().trim();
        denominacion = txtDenominacion.getText();
        frecuencia = txtFrecuencia.getText();
        paradasIntermedias = txtIntermedias.getText();

        int posPrimera = lstPrimera.getSelectedIndex();
        int posUltima = lstUltima.getSelectedIndex();
        if (!miEmpresa.buscarLinea(codigo)) {
            if (codigo.length() > 0 && denominacion.length() > 0
                    && frecuencia.length() > 0 && paradasIntermedias.length() > 0
                    && posPrimera > -1 && posUltima > -1) {
                /*el "-1" es para el jlist. Si no hay nada de selecionado, devuelve "-1"*/
                if (posPrimera != posUltima) {
                    p1 = miEmpresa.getUnaParada(posPrimera);
                    p2 = miEmpresa.getUnaParada(posUltima);

                    miLinea = new Linea(codigo, denominacion, frecuencia, p1.getDenominacion(), p2.getDenominacion(), paradasIntermedias);
                    miEmpresa.getLineas().add(miLinea);
                    miLinea.getParadas().add(p1);
                    miLinea.getParadas().add(p2);

                    limpiar();
                    JOptionPane.showMessageDialog(this, "LÍNEA AÑADIDA");
                } else {
                    JOptionPane.showMessageDialog(this, "ESA PARADA YA ESTÁ COGIDA");
                }
            } else {
                JOptionPane.showMessageDialog(this, "HAY CAMPOS VACIOS");
            }
        } else {
            JOptionPane.showMessageDialog(this, "YA EXISTE UNA LÍNEA CON ESE CÓDIGO");
            limpiar();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

