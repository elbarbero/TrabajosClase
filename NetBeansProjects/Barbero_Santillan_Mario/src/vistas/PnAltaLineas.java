    private void cargarPrimeraParada() {
        ArrayList<Paradas> miParada = miEmpresa.getParadas();
        for (int i = 0; i < miParada.size(); i++) {
            modeloPrimera.addElement(miParada.get(i));
        }
    }

    private void cargarUltimaParada() {
        ArrayList<Paradas> miParada = miEmpresa.getParadas();
        for (int i = 0; i < miParada.size(); i++) {
            int posPrimera = lstUltima.getSelectedIndex();
            if (i != posPrimera) {
                modeloUltima.addElement(miParada.get(i));
            }
        }
    }

