package modelo;

/**
 *
 * @author 203pc19
 */
public class Usuario {
    int codigo;
    String nombre,login,pass,tipoUsuario;

    public Usuario(int codigo, String nombre, String login, String pass, String tipoUsuario) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.login = login;
        this.pass = pass;
        this.tipoUsuario = tipoUsuario;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getTipoUsuario() {
        return tipoUsuario;
    }

    public void setTipoUsuario(String tipoUsuario) {
        this.tipoUsuario = tipoUsuario;
    }
    
    
    
}
