    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Asignatura a;
        int indice = lstAsigDisponibles.getSelectedIndex();
        if (indice == -1) {
            JOptionPane.showMessageDialog(this, "Primero selecciona una asignatura");
        } else {
            a = (Asignatura) modelo1.get(indice);
            modelo1.remove(indice);
            modelo2.addElement(a);//el "addElement" te lo agrega al final del siguiente elmento
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

