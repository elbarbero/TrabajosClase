    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Curso c;
        Asignatura a;
        String nombre, idCurso;
        idCurso = (String) txtCurso.getText();
        nombre = (String) txtDenominacion.getText();
        c = new Curso(idCurso, nombre);
        for (int i = 0; i < modelo2.size(); i++) {
            a = (Asignatura) modelo2.get(i);
            c.getAsignaturas().add(a);
        }
        miEmpresa.getCursos().add(c);
        JOptionPane.showMessageDialog(this, "CURSO CREADO");
        txtCurso.setText("");
        txtDenominacion.setText("");
        modelo2.removeAllElements();
        modelo1.removeAllElements();
        cargarAsignaturas();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void txtDenominacionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDenominacionKeyPressed
        switch (evt.getKeyCode()) {
            case 27://corresponde a la tecla ESCAPE
                txtDenominacion.setText("");
                break;
            case 112://corresponde a la tecla F1
                JOptionPane.showMessageDialog(this, "El nombre del curso");
        }
    }//GEN-LAST:event_txtDenominacionKeyPressed


