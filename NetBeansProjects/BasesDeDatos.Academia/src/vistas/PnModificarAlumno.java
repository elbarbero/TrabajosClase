    private void cmbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbAceptarActionPerformed
        Alumno a;

        GestorTablaCurso c = new GestorTablaCurso(bd);
        int nPlazasOcu = 0, nPlazasLibres = 0, nPlazasCursoTotales = 0;
        codCursoAlumnoActual = txtCursoActual.getText();//PARA CONTROLAR QUE NO ELIGA EL MISMO CURSO

        if (codCursoAlumnoActual.compareTo(codCursoNuevo) == 0) {
            JOptionPane.showMessageDialog(this, "ES EL MISMO CURSO");
        } else {
            nPlazasOcu = c.AlumnosPorCurso(codCursoNuevo);
            nPlazasCursoTotales = c.plazasCurso(codCursoNuevo);// ME DEVUELVE LAS PLAZAS TOTALES QUE TIENE EL CURSO NUEVO AL QUE SE QUIERE CAMBIAR
            nPlazasLibres = nPlazasCursoTotales - nPlazasOcu;// ME DEVUELVE LAS PLAZAS LIBRES QUE TIENE EL CURSO
            if (nPlazasOcu == nPlazasCursoTotales) {
                JOptionPane.showMessageDialog(this, "NO QUEDAN PLAZAS PARA ESTE CURSO");
            } else {
                a = new Alumno(codAlumno, codCursoNuevo);
                GestorTablaAlumno migestor = new GestorTablaAlumno(bd);
                registros = migestor.actualizarRegistros(a);
                if (registros != 0) {
                    JOptionPane.showMessageDialog(this, "ALUMNO MODIFICADO");
                    comboBoxAlumno();
                }
            }
        }

    }//GEN-LAST:event_cmbAceptarActionPerformed

