    private void cargarCursos() {
        GestorTablaCurso c = new GestorTablaCurso(bd);
        ArrayList<Curso> cursos;
        //lo hago así por un comboBox no es compatible con la clase GestorTablaCursos
        cursos = c.consultaTodosCursos();
        for (int i = 0; i < cursos.size(); i++) {
            modeloCursos.addElement(cursos.get(i));
        }
        /*modeloCursos.insertElementAt("------", 0);
        cmbCursos.setSelectedIndex(0);*/
    }

    private void cargarAlumnos() {
        GestorTablaAlumno a = new GestorTablaAlumno(bd);
        ArrayList<Alumno> alumnos;
        //lo hago así por un comboBox no es compatible con la clase GestorTablaAcademia
        alumnos = a.consultaTodosAlumnos();
        for (int i = 0; i < alumnos.size(); i++) {
            modeloAlumnos.addElement(alumnos.get(i));
        }
    }

    public void comboBoxAlumno() {
        if (registros > 0) {
            cmbAlumnos.removeAllItems();
            if (modeloAlumnos.getSize() == 0) {
                cargarAlumnos();
            }
            registros = 0;
        } else {
            miAlumno = (Alumno) modeloAlumnos.getSelectedItem();

            txtCursoActual.setText(miAlumno.getCodCurso());
            //codCursoAlumnoActual = txtCursoActual.getText();
            txtIdAlumno.setText(Integer.toString(miAlumno.getCodAlumno()));
            codAlumno = Integer.parseInt(txtIdAlumno.getText());
        }
    }

    public void comboBoxCurso() {
        miCurso = (Curso) cmbCursos.getSelectedItem();

        txtCursoNuevo.setText(miCurso.getCodCurso());
        codCursoNuevo = txtCursoNuevo.getText();
    }

