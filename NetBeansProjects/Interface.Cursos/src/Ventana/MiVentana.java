    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Curso c;//con el objeto "c" llamamos a la denomincacion de la clase Curso
        float importe;
        int indice = lstCursosOfertados.getSelectedIndex();
        if (indice == -1) {
            JOptionPane.showMessageDialog(this, "Primero selecciona el curso");
        } else {
            /*Ponemos "Curso" entre los parentesis porque en el metodo
             cargarCursos() hemos puesto "modelo1.addElement(c[i]);"
             Si ponemos puesto modelo1.addElement(c[i]).getDenominacion();
             en el método cargarCursos() tendriamos que poner:
             String c;
             c= (String)..., dependiendo del tipo de datos que sea*/
            c = (Curso) lstCursosOfertados.getSelectedValue();
            importe = (Float) lstPreciosOfertados.getSelectedValue();
            modelo3.addElement(c);//el "addElement" te lo agrega al final del siguiente elmento
            modelo4.addElement(importe);
            modelo1.remove(indice);
            /*modelo2 le decimos que está también en indice porque cuando seleccionamos
             el modelos1 (que es la denominacion), también seleccionamos automaticamente
             el modelo2 (que es el importe)*/
            modelo2.remove(indice);
            calculoImporte();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        Curso c;//con el objeto "c" llamamos a la denomincacion de la clase Curso
        float importe;
        int indice = lstCursosSelec.getSelectedIndex();
        if (indice == -1) {
            JOptionPane.showMessageDialog(this, "Primero selecciona el curso");
        } else {
            c = (Curso) lstCursosSelec.getSelectedValue();
            importe = (Float) lstPreciosSelec.getSelectedValue();
            modelo3.remove(indice);//el "remove"te lo borra
            modelo4.remove(indice);
            modelo1.addElement(c);
            /*modelo2 le decimos que está también en indice porque cuando seleccionamos
             el modelos1 (que es la denominacion), también seleccionamos automaticamente
             el modelo2 (que es el importe)*/
            modelo2.addElement(importe);
            calculoImporte();
        }
    }//GEN-LAST:event_btnQuitarActionPerformed

