    private void cargarCursos() {
        Curso[] c = miEmpresa.getCursos();//GUARDAMOS EN "c" TODOS LOS DATOS DE CADA CURSO
        for (int i = 0; i < c.length; i++) {
            modelo1.addElement(c[i]);// ASI NOS MUESTRA EL NOMBRE GRACIAS @Override DE LA CLASE CURSO
            modelo2.addElement(c[i].getImporte());
        }
    }

