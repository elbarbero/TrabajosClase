public class Comisiones {
    float importeHasta;
    float porcentaje;

    public Comisiones(float importeHasta, float porcentaje) {
        this.importeHasta = importeHasta;
        this.porcentaje = porcentaje;
    }

    public float getImporteHasta() {
        return importeHasta;
    }

    public float getPorcentaje() {
        return porcentaje;
    }
    
}
