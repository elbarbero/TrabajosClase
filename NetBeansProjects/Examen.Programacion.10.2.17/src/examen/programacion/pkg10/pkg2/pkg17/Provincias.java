public class Provincias {
    String nombre;
    int sueldoBase;

    public Provincias(String nombre, int sueldoBase) {
        this.nombre = nombre;
        this.sueldoBase = sueldoBase;
    }

    public String getNombre() {
        return nombre;
    }

    public int getSueldoBase() {
        return sueldoBase;
    }
    
}
