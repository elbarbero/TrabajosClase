    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        miAgenda = (Agenda) modeloEvento.getSelectedItem();
        int tipo;
        tipo = miAgenda.getTipo();

        int x = JOptionPane.showConfirmDialog(this, "¿DESEA ELIMINAR EL EVENTO?", "BORRAR EVENTOS",
                JOptionPane.YES_NO_OPTION);

        if (tipo == 0) {
            if (x == JOptionPane.YES_OPTION) {
                int pos = cmbNomEvento.getSelectedIndex();
                modeloEvento.removeElementAt(pos);
                validado.getbEntrada().remove(pos);
                JOptionPane.showMessageDialog(this, "EVENTO BORRADO");
            }
        } else {
            JOptionPane.showMessageDialog(this, "NO SE PUEDE BORRAR UN EVENTO DEL ADMIN");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

