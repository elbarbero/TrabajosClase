    private void txtMatriculaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMatriculaKeyPressed
        String matricula;
        GestorTblCoches migestor = new GestorTblCoches(bd);
        Coche c;
        if (evt.getKeyCode() == 10) {// el "10" corresponde a la tecla ENTER
            matricula = txtMatricula.getText().trim();
            c = migestor.existeUnCoche(matricula);
            if (c == null) {
                JOptionPane.showMessageDialog(this, "NO EXISTE LA MATRICULA");
                txtMatricula.setText("");
            } else {
                visualizar(c);
                deshabilitar();
            }
        }
    }//GEN-LAST:event_txtMatriculaKeyPressed

