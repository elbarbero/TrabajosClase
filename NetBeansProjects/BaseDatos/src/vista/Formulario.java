    private void btnAnularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnularActionPerformed
        int opcion;
        int registros;
        String matricula;
        GestorTblCoches migestor = new GestorTblCoches(bd);
        opcion = JOptionPane.showConfirmDialog(this, "¿DESEA BORRARLO?");
        if (opcion == 0) {
            matricula = txtMatricula.getText().trim();
            registros = migestor.eliminarUnCoche(matricula);
            if (registros != 0) {
                JOptionPane.showMessageDialog(this, "MATRICULA ELIMINADA");
                habilitar();
                limpiar();
            }
        }
        //migestor.cerrar();
    }//GEN-LAST:event_btnAnularActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String matricula, marca, modelo, color;
        float precio;
        Coche c;
        matricula = txtMatricula.getText();
        marca = txtMarca.getText();
        modelo = txtModelo.getText();
        color = txtColor.getText();
        precio = Float.parseFloat(txtPrecio.getText());
        c = new Coche(matricula, marca, modelo, color, precio);
        GestorTblCoches migestor = new GestorTblCoches(bd);
        int registros = migestor.actualizarRegistros(c);
        if (registros != 0) {
            JOptionPane.showMessageDialog(this, "COCHE MODIFICADO");
            habilitar();
            limpiar();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void limpiar() {
        txtColor.setText("");
        txtModelo.setText("");
        txtMarca.setText("");
        txtPrecio.setText("");
        txtMatricula.setText("");
        txtMatricula.requestFocus();
    }

    private void visualizar(Coche c) {
        txtColor.setText(c.getColor());
        txtModelo.setText(c.getModelo());
        txtMarca.setText(c.getMarca());
        /*para mostrar el precio hay pasarlo a un string porque
        el precio es un float*/
        txtPrecio.setText(String.valueOf(c.getPrecio()));
    }

